// --- 型定義 ---

export interface TarotSymbol {
  id: string;
  name: string;
  location: string; // カード内のどこにあるか
  highlightPassage: string; // ハイライト候補の文節（1文）
  description: string; // 詳細解説
}

export interface TarotCard {
  id: string;
  exhibitionNo: string;
  acquisitionYear: string;
  name: string;
  nameEn: string;
  symbol: string; // ローマ数字
  imagePath: string; // /tarot/XXX.jpg
  caption: string; // 展示キャプション（シンボル中心）
  symbols: TarotSymbol[]; // 各シンボルの詳細
}

export interface VtsDialogueTurn {
  question: string; // ファシリテーターの問い
  clientResponse: string; // 相談者の応答（模擬）
}

export interface ConsultationCase {
  id: string;
  caseNo: string;
  clientName: string;
  clientAvatar: string;
  clientTitle: string;
  text: string;
  status: 'unresolved' | 'pending' | 'solved';
  resolveAt?: number; // タイマー解放時刻（UNIX ms）
  sentCardId?: string; // 届けたカードのID
  sentHighlights?: string[]; // 届けたハイライト一節
  // カードIDごとの活動レポートテンプレート
  resolutionReports: Record<string, string>;
  // VTS対話スクリプト（カードIDごと）
  vtsDialogues: Record<string, VtsDialogueTurn[]>;
}

export interface FacilitatorProfile {
  name: string;
  title: string;
  avatar: string;
  greeting: string;
}

